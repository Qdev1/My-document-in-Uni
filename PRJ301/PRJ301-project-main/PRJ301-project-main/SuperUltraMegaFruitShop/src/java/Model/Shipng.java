package Model;

/**
 *
 * @author duonn
 */
public class Shipng {

    private int sid;
    private String name;
    private String email;
    private String phone;
    private String address;

    public Shipng() {
    }

    public Shipng(String name, String email, String phone, String address) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }

    public Shipng(int sid, String name, String email, String phone, String address) {
        this.sid = sid;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
